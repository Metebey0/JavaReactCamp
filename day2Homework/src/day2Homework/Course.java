package day2Homework;

public class Course {
	
	int id;
	String courseDetail;
	String instructor;
	boolean free;
	
	
	public Course() {
		
	}
	
	
	public Course(int id,String courseDetail,String instructor,boolean free) {
		
		super();
		this.id=id;
		this.courseDetail=courseDetail;
		this.instructor=instructor;
		this.free=free;
	}
	

}
